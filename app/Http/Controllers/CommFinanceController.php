     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {

        $commentaire = new ComFinance();
        $commentaire->comment= $request->get('comment');
        $commentaire->user()->associate($request->user());
        $financiere = Financiere::find($request->finance_id);
        $financiere->commentaires()->save($commentaire);


        return redirect()->back()->with('success','Commentaire ajout√©!');
    }

